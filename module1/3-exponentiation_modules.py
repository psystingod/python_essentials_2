from math import *;

# numero de euler
print(e);

# exponente de e a la x
print(exp(1));

# logaritmo natural de x
print(log(2));

# logaritmo de x en su base b
print(log(2, 10));

# logaritmo base 10 de x
print(log10(2));

# logaritmo base 2 de x, es mas preciso que log(x, 2)
print(log2(2));

# dos elevado a la potencia 4. pow no necesita ser importada
print(pow(2, 4));

